    <p>Lebih lanjut, dosen yang akrab disapa Mashuri ini menjelaskan,
        kegiatan mentoring ini terdiri dari beberapa rangkaian acara. Adapun
        diawali dengan dengan kuliah umum yang menghadirkan empat materi
        utama dari dosen-dosen agama Islam ITS sebagai pondasi awal. Setelah
        mendapatkan pemahaman dasar, mahasiswa mengikuti kelas pendalaman
        dalam enam sesi yang mencakup diskusi dan ujian untuk menguji
        pemahaman mereka.</p>
    <p>Dosen agama islam ITS Prof Dr Ir Adi Soeprijanto MT saat memberikan
        materi sirah nabawi pada Kuliah Umum Mentoring Agama Islam Semester
        Genap 2024/2025</p>
    <p>
        <img src="maba.jpg" width="400">
    </p>
    <p>Tak berhenti di situ, kegiatan ini kemudian diperkuat melalui
        kegiatan Ramadhan di Kampus (RDK) 46. Pada kegiatan ini, maba
        mengikuti kajian inspiratif, kajian rutin Subuh, dan Tarawih selama
        bulan Ramadhan guna memperdalam pemahaman keislaman secara
        aplikatif. “Melalui mentoring ini, mahasiswa mampu mengamalkannya
        ilmu keislaman dalam kehidupan sehari-hari,” tambah Dosen Statistika
        ITS tersebut.</p>
