
package prjprogdas2;

public class clsWhile1 {
    public static void main(String[] args) {
        System.out.println("Hallo apa kabar ?");
        int counter = 10 ; //start from
        while (counter >= 4 ){
            System.out.println(counter);
            counter = counter - 1 ;
        }
        System.out.println("Selesai");
    }
}
